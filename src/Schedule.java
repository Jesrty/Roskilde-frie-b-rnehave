public class Schedule {

	private String day;
	private int week;
	private int year;
	private String time;
	private String shift;

	public String getDay() {
		return this.day;
	}

	/**
	 * 
	 * @param day
	 */
	public void setDay(String day) {
		this.day = day;
	}

	public int getWeek() {
		return this.week;
	}

	/**
	 * 
	 * @param week
	 */
	public void setWeek(int week) {
		this.week = week;
	}

	public int getYear() {
		return this.year;
	}

	/**
	 * 
	 * @param year
	 */
	public void setYear(int year) {
		this.year = year;
	}

	public String getTime() {
		return this.time;
	}

	/**
	 * 
	 * @param time
	 */
	public void setTime(String time) {
		this.time = time;
	}

	public String getShift() {
		return this.shift;
	}

	/**
	 * 
	 * @param shift
	 */
	public void setShift(String shift) {
		this.shift = shift;
	}

}